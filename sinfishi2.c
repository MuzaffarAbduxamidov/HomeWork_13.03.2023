#include<stdio.h>

int main()
{
  int Son1 = 8;
  int Son2 = 4;

  printf("%d %c %d %c %d\n",Son1, '+', Son2, '=', Son1+Son2);
  printf("%d %c %d %c %d\n",Son1, '-', Son2, '=', Son1-Son2);
  printf("%d %c %d %c %d\n",Son1, '*', Son2, '=', Son1*Son2);
  printf("%d %c %d %c %d",Son1, '/', Son2, '=', Son1/Son2);
}
